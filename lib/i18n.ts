/**
 * Utility helper to fetch translated property based on language selection
 */
export function getTranslation<T extends Record<string, any>>(
  obj: T | null | undefined,
  field: string,
  lang: Language
): string {
  if (!obj) return "";
  if (typeof obj !== "object") return String(obj);
  
  // Field suffix naming e.g., titleEn, titleHi, titleGu
  const suffix = lang.charAt(0).toUpperCase() + lang.slice(1);
  const localizedField = `${field}${suffix}`;
  
  if (localizedField in obj && obj[localizedField]) {
    return obj[localizedField];
  }
  
  // Fallback to English version (e.g. titleEn)
  const englishField = `${field}En`;
  if (englishField in obj && obj[englishField]) {
    return obj[englishField];
  }
  
  // Standard fallback
  return obj[field] || "";
}
